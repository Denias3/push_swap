#include "push_swap.h"

int	diff_num(int *num, int len)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < len)
	{
		j = 0;
		while (j < len)
		{
			if (i != j)
				if (num[i] == num[j])
					return (0);
			j++;
		}
		i++;
	}
	return (1);
}
